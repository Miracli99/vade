const fs = require("fs");
const path = require("path");

const ROOT_DIR = path.resolve(__dirname, "..");
const ASSETS_DIR = path.join(ROOT_DIR, "assets");
const OUTPUT_FILE = path.join(ROOT_DIR, "src", "data", "image-library.ts");

const CATEGORY_CONFIG = [
  { folder: "characters", category: "character" },
  { folder: "spells", category: "spell" },
  { folder: "equipment", category: "equipment" },
  { folder: "inventory", category: "inventory" },
];

const IMAGE_EXTENSIONS = new Set([".png", ".jpg", ".jpeg", ".webp"]);

function toPosix(value) {
  return value.replace(/\\/g, "/");
}

function toSlug(value) {
  return value
    .toLowerCase()
    .replace(/[^a-z0-9]+/g, "-")
    .replace(/^-+|-+$/g, "");
}

function toLabel(relativeFilePath) {
  return relativeFilePath
    .replace(path.extname(relativeFilePath), "")
    .split(/[\\/]/)
    .flatMap((segment) => segment.split(/[_-]+/))
    .filter(Boolean)
    .map((part) => part.charAt(0).toUpperCase() + part.slice(1))
    .join(" ");
}

function toTagParts(relativeFilePath, category) {
  return [
    category,
    ...relativeFilePath
      .replace(path.extname(relativeFilePath), "")
      .split(/[\\/]/)
      .flatMap((segment) => segment.split(/[_-]+/))
      .map((part) => part.toLowerCase().trim())
      .filter(Boolean),
  ];
}

function collectFiles(directory) {
  if (!fs.existsSync(directory)) {
    return [];
  }

  const entries = fs.readdirSync(directory, { withFileTypes: true });
  const files = [];

  for (const entry of entries) {
    const fullPath = path.join(directory, entry.name);

    if (entry.isDirectory()) {
      files.push(...collectFiles(fullPath));
      continue;
    }

    if (!entry.isFile()) {
      continue;
    }

    if (!IMAGE_EXTENSIONS.has(path.extname(entry.name).toLowerCase())) {
      continue;
    }

    files.push(fullPath);
  }

  return files;
}

function buildCategoryEntries(folder, category) {
  const folderPath = path.join(ASSETS_DIR, folder);
  const thumbnailFolderPath = path.join(ASSETS_DIR, "thumbnails", folder);

  return collectFiles(folderPath)
    .map((fullPath) => {
      const relativeFromFolder = path.relative(folderPath, fullPath);
      const relativeFromOutput = path.relative(path.dirname(OUTPUT_FILE), fullPath);
      const thumbnailPath = path.join(
        thumbnailFolderPath,
        relativeFromFolder.replace(path.extname(relativeFromFolder), ".png"),
      );
      const thumbnailRequirePath = fs.existsSync(thumbnailPath)
        ? toPosix(path.relative(path.dirname(OUTPUT_FILE), thumbnailPath))
        : null;
      const idParts = [
        category === "character" ? "char" : category === "spell" ? "spell" : category === "equipment" ? "equip" : "item",
        toSlug(relativeFromFolder.replace(path.extname(relativeFromFolder), "")),
      ].filter(Boolean);

      return {
        id: idParts.join("-"),
        label: toLabel(relativeFromFolder),
        category,
        tags: Array.from(new Set(toTagParts(relativeFromFolder, category))).sort(),
        requirePath: toPosix(relativeFromOutput),
        thumbnailRequirePath,
      };
    })
    .sort((left, right) => left.requirePath.localeCompare(right.requirePath));
}

function renderEntries(entries) {
  if (!entries.length) {
    return "[]";
  }

  const rendered = entries.map(
    (entry) => `    {
      id: "${entry.id}",
      label: "${entry.label}",
      category: "${entry.category}",
      tags: ${JSON.stringify(entry.tags)},
      imageModule: require("${entry.requirePath}"),
      ${entry.thumbnailRequirePath ? `thumbnailModule: require("${entry.thumbnailRequirePath}"),` : ""}
    }`,
  );

  return `[\n${rendered.join(",\n")}\n  ]`;
}

function buildFileContents() {
  const categoryEntries = Object.fromEntries(
    CATEGORY_CONFIG.map(({ folder, category }) => [category, buildCategoryEntries(folder, category)]),
  );

  return `// This file is auto-generated by scripts/generate-image-library.js.
// Do not edit it by hand.

export type ImageLibraryCategory =
  | "character"
  | "spell"
  | "equipment"
  | "inventory";

export type LocalImageOption = {
  id: string;
  label: string;
  category: ImageLibraryCategory;
  tags: string[];
  imageModule: number;
  thumbnailModule?: number;
};

export const LOCAL_IMAGE_LIBRARY: Record<ImageLibraryCategory, LocalImageOption[]> = {
  character: ${renderEntries(categoryEntries.character)},
  spell: ${renderEntries(categoryEntries.spell)},
  equipment: ${renderEntries(categoryEntries.equipment)},
  inventory: ${renderEntries(categoryEntries.inventory)},
};
`;
}

fs.mkdirSync(path.dirname(OUTPUT_FILE), { recursive: true });
fs.writeFileSync(OUTPUT_FILE, buildFileContents(), "utf8");

console.log(`Generated ${path.relative(ROOT_DIR, OUTPUT_FILE)}`);
